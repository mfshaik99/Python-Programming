# Input user data
name = input("Enter your name: ")
age = int(input("Enter your age: "))

# String and arithmetic operations
doubled_name = name * 2     # repeats the name
age_in_5_years = age + 5    # adds 5 to age
name_length = len(name)     # counts characters in name

# Output
print("Your name twice:", doubled_name)
print("Your age after 5 years:", age_in_5_years)
print("Number of characters in your name:", name_length)
