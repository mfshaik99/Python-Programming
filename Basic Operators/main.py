# Arithmetic Operators
a = 10
b = 5
print(f"Addition: {a + b}")  # 15
print(f"Subtraction: {a - b}")  # 5
print(f"Multiplication: {a * b}")  # 50
print(f"Division: {a / b}")  # 2.0

# Relational Operators
print(f"Equal: {a == b}")  # False
print(f"Greater than: {a > b}")  # True
print(f"Less than: {a < b}")  # False

# Logical Operators
print(f"AND: {True and False}")  # False
print(f"OR: {True or False}")  # True
print(f"NOT: {not True}")  # False

# Assignment Operators
x = 10
x += 5  # x = x + 5
print(f"New x: {x}")  # 15
